package com.crowdfunding.common;

public enum ResultEnum {
    SUCCESS(1,"success"),ERROR(0,"error");
    private int code;
    private String message;

    ResultEnum(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}
